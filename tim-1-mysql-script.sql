-- PRIKAZI KOJA JE OPCINA NAJVISE ZARADILA

CREATE VIEW zarada_po_parceli AS
SELECT prodaja_id, (kolicina_prodaja * cijena) AS zarada_clana_po_parceli
	FROM prodaja NATURAL JOIN clan
    GROUP BY prodaja_id
    ORDER BY prodaja_id;

CREATE VIEW zarada_clana AS
SELECT clan_id, SUM(zarada_clana_po_parceli) AS zarada
	FROM prodaja NATURAL JOIN zarada_po_parceli
    GROUP BY clan_id;
    

CREATE VIEW zarada_opcine AS
SELECT opcina, SUM(zarada) AS zarada_opcine
	FROM zarada_clana NATURAL JOIN mjesto NATURAL JOIN parcela
    GROUP BY opcina;
    
SELECT * 
	FROM zarada_opcine
    ORDER BY zarada_opcine DESC
    LIMIT 1;

-- PRIKAZI CIJENU MASLINOVOG ULJA ZA CLANOVE KOJI SU BILI NA RADIONICI MARKETING MASLINOVOG ULJA

