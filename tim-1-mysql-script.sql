-- PRIKAZI KOJI CLANOVI IMAJU NAJVECU KOLICINU OPREME ZA SVAKU VRSTU
-- DOLAZI DO PROBLEMA JER POJEDINI CLANOVI IMAJU ISTU KOLICINU OPREME.

CREATE VIEW max_kolicina_opreme AS 

SELECT oprema_id, MAX(kolicina) AS broj
	FROM oprema_od_clana NATURAL JOIN oprema
    GROUP BY oprema_id, vrsta_opreme;


SELECT clan_id, ime, prezime, vrsta_opreme, kolicina
	FROM max_kolicina_opreme 
    NATURAL JOIN oprema_od_clana 
    NATURAL JOIN clan 
    NATURAL JOIN oprema
    WHERE max_kolicina_opreme.oprema_id = oprema_od_clana.oprema_id 
    AND max_kolicina_opreme.broj = oprema_od_clana.kolicina
    ORDER BY vrsta_opreme;

-- PRIKAZI ZARADU ULJARE (PRETPOSTAVIMO DA SVAKO NASELJE IMA SVOJU ULJARU I CIJENA PRERADE PO KG JE 0.13 EURA) 

CREATE VIEW kg_maslina_po_naselju AS 
SELECT naselje, SUM(kg_maslina) AS kg_po_naselju
	FROM mjesto NATURAL JOIN parcela NATURAL JOIN berba
    GROUP BY naselje;
    
SELECT naselje, kg_po_naselju * 0.13 AS zarada_uljare
	FROM kg_maslina_po_naselju;

-- PRIKAZI UKUPAN PROFIT SVAKOG CLANA, PROFIT JE OSTVAREN PRODAJOM ULJA I ODUZIMANJEM TROSKA ŠTO JE ZAPRAVO KUPLJENA OPREMA 
-- PROBLEM NASTAJE U TOME ŠTO ĆE SVI CLANOVI BITI U MINUSU RADI PRIKAZA PRIHODA SAMO JEDNE KALENDARSKE GODINE.

CREATE VIEW broj_opreme_po_clanu AS 
SELECT clan_id, oprema_id, SUM(kolicina) AS broj_opreme
	FROM oprema_od_clana
	GROUP BY oprema_id, clan_id
    ORDER BY clan_id;

CREATE VIEW trosak_za_opremu AS
SELECT clan_id, oprema_id, (broj_opreme * cijena_opreme) AS trosak
	FROM broj_opreme_po_clanu NATURAL JOIN oprema
    GROUP BY clan_id, oprema_id
    ORDER BY clan_id;
    
CREATE VIEW trosak_clana AS
SELECT clan_id, SUM(trosak) AS sveukupni_trosak
	FROM trosak_za_opremu
	GROUP BY clan_id
    ORDER BY clan_id;
    
CREATE VIEW temp AS
SELECT clan_id, SUM(zarada_clana_po_parceli) AS zarada
	FROM prodaja NATURAL JOIN zarada_po_parceli
    GROUP BY clan_id;

SELECT clan_id, (zarada - sveukupni_trosak) AS prihod 
	FROM temp NATURAL JOIN trosak_clana
    GROUP BY clan_id
    ORDER BY clan_id;
    
    
