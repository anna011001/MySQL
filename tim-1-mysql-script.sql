DROP DATABASE IF EXISTS udruga_maslinara;
CREATE DATABASE udruga_maslinara;
USE udruga_maslinara;

CREATE TABLE clan (
		clan_id INTEGER AUTO_INCREMENT PRIMARY KEY,
    ime VARCHAR(30) NOT NULL,
    prezime VARCHAR(30) NOT NULL, 
    telefon INTEGER NOT NULL UNIQUE,
    adresa VARCHAR(50) NOT NULL
);

CREATE TABLE mjesto (
		mjesto_id INTEGER AUTO_INCREMENT PRIMARY KEY,
    opcina VARCHAR(15) NOT NULL,
    naselje VARCHAR(15) NOT NULL
);

CREATE TABLE clanarina (
		clanarina_id INTEGER AUTO_INCREMENT PRIMARY KEY,
    datum_clanarina DATE DEFAULT NULL,
    dug INTEGER CHECK (dug = 0 OR dug = 50) DEFAULT 50,
    clan_id INTEGER,
    FOREIGN KEY (clan_id) REFERENCES clan(clan_id) ON DELETE CASCADE
);

CREATE TABLE parcela (
		parcela_id INTEGER AUTO_INCREMENT PRIMARY KEY,
    mjesto_id INTEGER,
    clan_id INTEGER,
    broj_maslina INTEGER NOT NULL CHECK (broj_maslina > 0 AND broj_maslina < 1000),
    povrsina INTEGER NOT NULL CHECK (povrsina > 0 AND povrsina < 10000),
    FOREIGN KEY (mjesto_id) REFERENCES mjesto(mjesto_id),
    FOREIGN KEY (clan_id) REFERENCES clan(clan_id) ON DELETE CASCADE
);

