CREATE TABLE ulje (
		ulje_id INTEGER AUTO_INCREMENT PRIMARY KEY,
    nagrade VARCHAR(30) DEFAULT NULL,
    clan_id INTEGER,
    FOREIGN KEY (clan_id) REFERENCES clan(clan_id) ON DELETE CASCADE

);

CREATE TABLE prodaja (
		prodaja_id INTEGER AUTO_INCREMENT PRIMARY KEY,
    kolicina_prodaja INTEGER NOT NULL CHECK (kolicina_prodaja > 0 AND kolicina_prodaja < 500),
    cijena NUMERIC(5,2) NOT NULL CHECK (cijena > 10 AND cijena < 40),
    clan_id INTEGER,
    FOREIGN KEY (clan_id) REFERENCES clan(clan_id) ON DELETE CASCADE
    
);

CREATE TABLE radionica (
		radionica_id INTEGER AUTO_INCREMENT PRIMARY KEY,
    predavac VARCHAR(30) NOT NULL,
    tema VARCHAR(40) NOT NULL,
    datum_radionica DATE NOT NULL
);

CREATE TABLE clan_na_radionici (
		cnr_id INTEGER AUTO_INCREMENT PRIMARY KEY,
    radionica_id INTEGER,
    clan_id INTEGER,
    FOREIGN KEY (radionica_id) REFERENCES radionica(radionica_id),
    FOREIGN KEY (clan_id) REFERENCES clan(clan_id) ON DELETE CASCADE
);

CREATE TABLE berba (
		berba_id INTEGER AUTO_INCREMENT PRIMARY KEY,
    datum_berba DATE NOT NULL,
    kg_maslina INTEGER NOT NULL CHECK (kg_maslina > 0 AND kg_maslina < 10000),
    parcela_id INTEGER,
    FOREIGN KEY (parcela_id) REFERENCES parcela(parcela_id) ON DELETE CASCADE
);

CREATE TABLE clan_u_berbi (
		cub_id INTEGER AUTO_INCREMENT PRIMARY KEY,
    berba_id INTEGER,
    clan_id INTEGER,
    FOREIGN KEY (berba_id) REFERENCES berba(berba_id) ON DELETE CASCADE,
    FOREIGN KEY (clan_id) REFERENCES clan(clan_id) ON DELETE CASCADE
);

CREATE TABLE oprema (
		oprema_id INTEGER AUTO_INCREMENT PRIMARY KEY,
    vrsta_opreme VARCHAR(20) NOT NULL,
    cijena_opreme NUMERIC(6,2) NOT NULL
);

CREATE TABLE oprema_od_clana ( 
		ooc_id INTEGER AUTO_INCREMENT PRIMARY KEY,
    oprema_id INTEGER,
    kolicina INTEGER CHECK (kolicina < 20) DEFAULT NULL,
    clan_id INTEGER,
    FOREIGN KEY (oprema_id) REFERENCES oprema(oprema_id),
    FOREIGN KEY (clan_id) REFERENCES clan(clan_id) ON DELETE CASCADE
);
  
CREATE TABLE oprema_na_berbi (
		onb_id INTEGER AUTO_INCREMENT PRIMARY KEY,
    oprema_id INTEGER,
    kolicina_onb INTEGER CHECK (kolicina_onb < 20) DEFAULT NULL,
    berba_id INTEGER,
    FOREIGN KEY (oprema_id) REFERENCES oprema(oprema_id),
    FOREIGN KEY (berba_id) REFERENCES berba(berba_id) ON DELETE CASCADE
);


CREATE TABLE maslina_na_parceli ( 
		mnp_id INTEGER AUTO_INCREMENT PRIMARY KEY,
    dob INTEGER NOT NULL CHECK (dob > 0 AND dob < 1000), 
    parcela_id INTEGER,
    clan_id INTEGER, 
    vrsta VARCHAR(20) NOT NULL,
    FOREIGN KEY (parcela_id) REFERENCES parcela(parcela_id) ON DELETE CASCADE,
    FOREIGN KEY (clan_id)    REFERENCES clan(clan_id) ON DELETE CASCADE
); 
    
CREATE TABLE prerada (
		prerada_id INTEGER AUTO_INCREMENT PRIMARY KEY,
    datum DATE NOT NULL,
    litara_ulja NUMERIC(5,2) NOT NULL CHECK (litara_ulja > 0 AND litara_ulja < 200),
    parcela_id INTEGER,
    FOREIGN KEY (parcela_id) REFERENCES parcela(parcela_id) ON DELETE CASCADE
);


CREATE TABLE odrzavanje_na_parceli (
	onp_id INTEGER AUTO_INCREMENT PRIMARY KEY,
vrsta_odrzavanja VARCHAR(20) DEFAULT NULL,
parcela_id INTEGER,
datum DATE DEFAULT NULL, 
FOREIGN KEY (parcela_id) REFERENCES parcela(parcela_id) ON DELETE CASCADE
