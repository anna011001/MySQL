FROM maslina_na_parceli NATURAL JOIN berba
	GROUP BY vrsta;


-- KOJE OPCINA JE IMALO SVEUKUPNO NAJVIŠE STABALA MASLINA
CREATE VIEW broj_maslina_po_opcini AS 
SELECT opcina, SUM(broj_maslina) AS Ukupan_broj_maslina
	FROM mjesto INNER JOIN parcela WHERE mjesto.mjesto_id=parcela.mjesto_id 
    GROUP BY opcina;

SELECT opcina
FROM broj_maslina_po_opcini
WHERE Ukupan_broj_maslina = (SELECT MAX(Ukupan_broj_maslina) FROM broj_maslina_po_opcini);


-- KOLIKO JE SVAKA VRSTA MASLINE IMALA POSTOTAK ULJA

CREATE VIEW sveukupno_kilograma AS 
SELECT vrsta, SUM(kg_maslina) AS sveukupno_kg, SUM(litara_ulja) AS sveukupno_ulja
FROM maslina_na_parceli NATURAL JOIN berba NATURAL JOIN prerada
GROUP BY vrsta;

SELECT vrsta, sveukupno_ulja / (sveukupno_kg / 100)  AS postotak_ulja 
	FROM sveukupno_kilograma;
    
    
